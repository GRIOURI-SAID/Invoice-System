    public function getInvoiceDetails($id){

         $invoice = invocie::find($id);

         $invoices_details = invoices_details::where("id_invoice",$id)->get();
       $invoices_attachments= invoices_attachment::where("invoice_id",$id)->get();

        return view("invoices.detail_Invoice" ,[
            "invoice" => $invoice,
            "invoices_details" => $invoices_details,
            "invoices_attachments" =>$invoices_attachments
        ]);
